//------------------------------------------------------------------------------
// Public Functions
//------------------------------------------------------------------------------

char get_key(void)
{
    uint8_t c = inb(0x64);
    if (c & 0x01) {
        c = inb(0x60);
        if (c < sizeof(keymap)) {
            return keymap[c];
        }
        // Ignore keys we don't recognise and key up codes
    }
    return '\0';
}
